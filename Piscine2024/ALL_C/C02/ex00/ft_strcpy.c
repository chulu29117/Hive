#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char	dest[20];

	printf("Str original (str): %s\n", "Hello");
	ft_strcpy(dest, "Hello");
	printf("Str copied (dest): %s\n", dest);
	return (0);
}
