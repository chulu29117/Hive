#include <stdio.h>

char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	j = 0;
	while (src[j])
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char	dest[50] = "Hello";
	char	src[] = " Bitch";
	
	printf("dest: %s src: %s\n", dest, src);
	printf("Dest: %s\n", ft_strcat(dest, src));
	return (0);
}
